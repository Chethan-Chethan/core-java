class Bikes {
	
	private String name;
	private double cost;
	private int speed;
	
	public void setName(String bikeName) {
		name = bikeName;
	}
	
	public void setCost(double bikeCost) {
		cost = bikeCost;
	}
	
	public void setSpeed(int bikeSpeed) {
		speed = bikeSpeed;
	}
	
	public String getName() {
		return name;
	}
		
	public double getCost() {
		return cost;
	}
	
	public int getSpeed() {
		return speed;
	}
}

	

		