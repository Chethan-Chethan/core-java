class Elephant {
	private String name;
	private int age;
	private int weight;
	
	public Elephant() {
		this.name = "Abimanyu";
		this.age = 55;
		this.weight = 506;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
	
	public int getAge() {
		return age;
	}
	
	public void setWeight(int weight) {
		this.weight = weight;
	}
	
	public int getWeight() {
		return weight;
	}
	
	
	
}